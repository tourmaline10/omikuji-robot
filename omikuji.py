# おみくじロボット
# 実行するとランダムに今日の運勢を表示します

import random

omikuji = [
    "大吉 🎉",
    "中吉 😊",
    "小吉 🙂",
    "吉 😌",
    "末吉 🤏",
    "凶 😢",
    "大凶 💀"
]

messages = {
    "大吉 🎉": "今日は最高の一日！新しいことに挑戦しよう。",
    "中吉 😊": "良い流れ。焦らず進めばOK。",
    "小吉 🙂": "小さな幸せを大切に。",
    "吉 😌": "安定した一日。コツコツが吉。",
    "末吉 🤏": "後半に運気アップ。油断しないで。",
    "凶 😢": "無理せず慎重に行動しよう。",
    "大凶 💀": "今日は休憩日。安全第一！"
}

result = random.choice(omikuji)

print("=== おみくじロボット ===")
print("今日の運勢：", result)
print(messages[result])
